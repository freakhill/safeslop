// TestWriteCatalogAtomicNoTempResidue proves the atomic write path leaves exactly the two
// artifacts (0644) and no leftover temp files — the backstop for specs/0059 D1's
// never-drift guarantee. A leftover ".catalog.json.tmp-*" would signal a torn write or a
// missing cleanup path.
func TestWriteCatalogAtomicNoTempResidue(t *testing.T) {
	orig, err := LoadCatalogFile("catalog.json")
	if err != nil {
		t.Fatalf("load real catalog: %v", err)
	}
	dir := t.TempDir()
	tmpCue := filepath.Join(dir, "catalog.cue")
	tmpJSON := filepath.Join(dir, "catalog.json")
	if err := WriteCatalog(tmpCue, tmpJSON, orig); err != nil {
		t.Fatalf("WriteCatalog returned error: %v", err)
	}
	entries, err := os.ReadDir(dir)
	if err != nil {
		t.Fatalf("read temp dir: %v", err)
	}
	var names []string
	for _, e := range entries {
		names = append(names, e.Name())
		if strings.Contains(e.Name(), ".tmp-") {
			t.Fatalf("leftover temp file after WriteCatalog: %q", e.Name())
		}
	}
	if len(names) != 2 {
		t.Fatalf("WriteCatalog left %v, want exactly catalog.cue + catalog.json", names)
	}
	for _, p := range []string{tmpCue, tmpJSON} {
		info, err := os.Stat(p)
		if err != nil {
			t.Fatalf("stat %s: %v", p, err)
		}
		if info.Mode().Perm() != 0o644 {
			t.Fatalf("%s mode = %v, want 0644", p, info.Mode().Perm())
		}
	}
}

func assertCatalogIORenderSync(t *testing.T, cuePath, jsonPath string) {
	t.Helper()
	cueBytes, err := os.ReadFile(cuePath)
	if err != nil {
		t.Fatalf("read %s: %v", cuePath, err)
	}
	rendered, err := renderCatalogToJSON(cueBytes)
	if err != nil {
		t.Fatalf("render emitted cue: %v", err)
	}
	jsonBytes, err := os.ReadFile(jsonPath)
	if err != nil {
		t.Fatalf("read %s: %v", jsonPath, err)
	}
	if !bytes.Equal(rendered, jsonBytes) {
		t.Fatalf("rendered cue JSON does not match emitted catalog.json\nrendered:\n%s\njson:\n%s", rendered, jsonBytes)
	}
}

func assertCatalogIOSame(t *testing.T, got, want *Catalog) {
	t.Helper()
	gotData := catalogIOSnapshot(got)
	wantData := catalogIOSnapshot(want)
	if !reflect.DeepEqual(gotData, wantData) {
		t.Fatalf("catalog mismatch\ngot=%#v\nwant=%#v", gotData, wantData)
	}
}

func catalogIOSnapshot(c *Catalog) catalogData {
	pkgs := c.Packages()
	for i := range pkgs {
		pkgs[i] = clonePackage(pkgs[i])
	}
	bundles := c.Bundles()
	for i := range bundles {
		bundles[i].Packages = cloneStringSlice(bundles[i].Packages)
	}
	defaults := cloneStringMap(c.defaults)
	if defaults == nil {
		defaults = map[string]string{}
	}
	return catalogData{Packages: pkgs, Bundles: bundles, Defaults: defaults}
}

func catalogIODigest(s string) string { return strings.Repeat(s, 64) }

func firstCatalogIOLines(b []byte, n int) string {
	lines := strings.Split(string(b), "\n")
	if len(lines) > n {
		lines = lines[:n]
	}
	return strings.Join(lines, "\n")
}
